    <h2>Depardieu : Le site</h2><br>
    <p><strong>Gérard Depardieu</strong>, né <em>le 27 décembre 1948 à Châteauroux</em>, est un <b>acteur, chanteur, réalisateur,
            producteur de cinéma, de
            télévision et de théâtre français.</b> Il est aussi <em>homme d'affaires</em>, notamment dans le domaine de
        la viticulture.</p>

    <p>Révélé en 1974 avec <strong>Patrick Dewaere</strong> par le film <em>Les Valseuses</em>, il est depuis l'un des comédiens
        les plus actifs et l'une des
        principales vedettes du cinéma français.</p>

    <p>Il a tourné avec des cinéastes français prestigieux, parmi lesquels François Truffaut, Jean-Luc
        Godard, André Téchiné ou
        Alain Resnais, mais également étrangers tels que Bernardo Bertolucci, Marco Ferreri, Andrzej
        Wajda ; et a alterné films
        d'auteur et divertissements populaires, tout en apparaissant également dans des films
        hollywoodiens durant les années 1990
        (Green Card de Peter Weir, 1492 : Christophe Colomb de Ridley Scott, L'Homme au masque de fer de
        Randall Wallace). Il fut
        en outre l'acteur fétiche des réalisateurs Maurice Pialat, Francis Veber, Alain Corneau et
        Bertrand Blier. Apparu dans plus
        de deux cents films, il est le deuxième acteur français ayant cumulé le plus d'entrées en
        France, derrière Louis de Funès.
        En 1981, son interprétation dans Le Dernier métro lui permet d'obtenir son premier César du
        meilleur acteur. Il obtient l'un
        de ses plus grands succès en 1990 avec Cyrano de Bergerac, qui lui vaut son deuxième César du
        meilleur acteur, le prix
        d'interprétation masculine du Festival de Cannes et une nomination à l'Oscar du meilleur acteur.
    </p>

    <p>Il a par ailleurs réalisé ou coréalisé trois films : Le Tartuffe en 1984, Un pont entre deux
        rives
        (avec Frédéric Auburtin) en 1999 et le segment Quartier Latin dans Paris je t'aime en 2006.</p>

    <p>Il est le père des acteurs Guillaume et Julie Depardieu. </p>
